#include "../../inc/libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*memory;

	if (count && size && count > (((size_t) - 1) / size))
		return (NULL);
	memory = (void *)malloc(count * size);
	if (!memory)
		return (NULL);
	ft_bzero(memory, count * size);
	return (memory);
}
